  const form = document.querySelector(".contact-form");

  form.addEventListener("submit", async (e) => {
    e.preventDefault(); // page reload stop pannum

    const formData = new FormData(form);

    const response = await fetch(form.action, {
      method: form.method,
      body: formData,
      headers: { 'Accept': 'application/json' }
    });

    // Message element create pannrom
    let msg = document.createElement("p");
    msg.style.marginTop = "15px";
    msg.style.fontWeight = "600";
    msg.style.textAlign = "center";

    if (response.ok) {
      msg.textContent = "✅ Message sent successfully!";
      msg.style.color = "limegreen";
      form.reset();
    } else {
      msg.textContent = "❌ Failed to send. Please try again!";
      msg.style.color = "crimson";
    }

    // Old message irundha remove pannu
    const oldMsg = document.querySelector(".form-message");
    if (oldMsg) oldMsg.remove();

    msg.classList.add("form-message");
    form.appendChild(msg);

    // 5 seconds la auto remove aagum
    setTimeout(() => msg.remove(), 5000);
  });


